package stringJava;

public class LastIndexOfString 
{

	public static void main(String[] args)
	{
		String s1 = "Welcome";
	
		
		int lastIndex = s1.lastIndexOf('e');
		System.out.println(lastIndex);
		
		int Index = s1.lastIndexOf('e', 5);
		System.out.println(Index);
		

	}

}
